package com.java;

public class Concat {

	public static void main(String[] args) {
		int a=100;
		System.out.println("a="+a);
		System.out.println(100+"abcd");
        System.out.println("1"+2+"3");
        System.out.println(1+"34"+5);
        System.out.println(1+4+"55");
        System.out.println('d'+100);  //ASCII value of d=100
	}

}
